//write a program to print size of- char,int,float,double,short int, long int
#include<iostream>
using namespace std;
int main(){
	char ch;
	int a;
	float b;
	double c;
	short int aa;
	long int aaa;
	cout << "Size of char:"<< sizeof(ch)<< endl;
	cout << "Size of integer:"<< sizeof(a)<< endl;
	cout << "Size of float:"<< sizeof(b)<< endl;
	cout << "Size of double:"<< sizeof(c)<< endl;
	cout << "Size of short int:"<< sizeof(aa)<< endl;
	cout << "Size of long int:"<< sizeof(aaa)<< endl;
	return 0;
	
}

